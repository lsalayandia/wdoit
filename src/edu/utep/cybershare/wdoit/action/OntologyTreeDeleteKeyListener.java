package edu.utep.cybershare.wdoit.action;

import java.awt.event.KeyEvent;
import java.awt.event.KeyListener;

import edu.utep.cybershare.wdoit.WdoApp;
import edu.utep.cybershare.wdoit.ui.WdoView;

/**
 * Listens for Delete-key pressed events occurring on wdo:Data and wdo:Method
 * trees. When triggered, calls method to delete selected wdo concept.
 * 
 * @author Leonardo Salayandia
 */
public class OntologyTreeDeleteKeyListener implements KeyListener {

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.KeyListener#keyPressed(java.awt.event.KeyEvent)
	 */
	@Override
	public void keyPressed(KeyEvent e) {
		if (e.getKeyCode() == KeyEvent.VK_DELETE) {
			WdoView wdoView = (WdoView) WdoApp.getApplication().getMainView();
			wdoView.removeWDOConcept();
		}
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.KeyListener#keyReleased(java.awt.event.KeyEvent)
	 */
	@Override
	public void keyReleased(KeyEvent e) {

	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.awt.event.KeyListener#keyTyped(java.awt.event.KeyEvent)
	 */
	@Override
	public void keyTyped(KeyEvent e) {

	}
}
